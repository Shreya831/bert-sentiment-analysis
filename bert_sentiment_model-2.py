import gradio as gr
from transformers import pipeline

# Load your trained model
classifier = pipeline("sentiment-analysis", model="./bert_sentiment_model", tokenizer="./bert_sentiment_model")

# Function to predict sentiment
def predict_sentiment(text):
    result = classifier(text)[0]  # returns a list with dict
    return f"Label: {result['label']}, Score: {result['score']:.3f}"

# Create Gradio interface
demo = gr.Interface(
    fn=predict_sentiment,
    inputs=gr.Textbox(lines=3, placeholder="Enter text here..."),
    outputs="text",
    title="BERT Sentiment Classifier Demo"
)

# Launch demo with public URL
demo.launch(share=True)

model.save_pretrained("bert_sentiment_model")
tokenizer.save_pretrained("bert_sentiment_model")

