import torch
from transformers import BertTokenizer, BertForSequenceClassification, Trainer, TrainingArguments
from datasets import load_dataset
from sklearn.metrics import accuracy_score, f1_score

dataset = load_dataset("imdb")  # Download IMDB dataset
train_dataset = dataset["train"]
test_dataset = dataset["test"]

print(train_dataset[0])  # See one example

tokenizer = BertTokenizer.from_pretrained("bert-base-uncased")

def tokenize(batch):
    return tokenizer(batch['text'], padding=True, truncation=True, max_length=128)

train_dataset = train_dataset.map(tokenize, batched=True)
test_dataset = test_dataset.map(tokenize, batched=True)

# Format for PyTorch
train_dataset.set_format('torch', columns=['input_ids', 'attention_mask', 'label'])
test_dataset.set_format('torch', columns=['input_ids', 'attention_mask', 'label'])

model = BertForSequenceClassification.from_pretrained("bert-base-uncased", num_labels=2)

def compute_metrics(pred):
    labels = pred.label_ids
    preds = pred.predictions.argmax(-1)
    acc = accuracy_score(labels, preds)
    f1 = f1_score(labels, preds, average='weighted')
    return {'accuracy': acc, 'f1': f1}

training_args = TrainingArguments(
    output_dir='./results',         # Save model here
    num_train_epochs=2,             # You can increase to 3-4 if GPU available
    per_device_train_batch_size=16, # Depends on GPU memory
    per_device_eval_batch_size=32,
    warmup_steps=500,
    weight_decay=0.01,
    logging_dir='./logs',           # Reverting to logging_dir to fix TypeError
    eval_strategy="epoch",
    save_strategy="epoch",
    load_best_model_at_end=True
)

train_dataset_small = train_dataset.select(range(2000))
test_dataset_small = test_dataset.select(range(500))

trainer = Trainer(
    model=model,
    args=training_args,
    train_dataset=train_dataset_small,
    eval_dataset=test_dataset_small,
    compute_metrics=compute_metrics
)

results = trainer.evaluate()
print(results)

model.save_pretrained("./bert_sentiment_model")
tokenizer.save_pretrained("./bert_sentiment_model")

